-- 2.1 Task Execution Summary
CREATE OR REPLACE VIEW V_TASK_EXECUTION_SUMMARY AS
SELECT
    DATABASE_NAME,
    SCHEMA_NAME,
    NAME AS TASK_NAME,
    STATE,
    DATE_TRUNC('day', SCHEDULED_TIME) AS SCHEDULED_DATE,
    COUNT(*) AS TOTAL_RUNS,
    SUM(CASE WHEN STATE = 'SUCCEEDED' THEN 1 ELSE 0 END) AS SUCCESS_COUNT,
    SUM(CASE WHEN STATE = 'FAILED' THEN 1 ELSE 0 END) AS FAILURE_COUNT,
    SUM(CASE WHEN STATE = 'CANCELLED' THEN 1 ELSE 0 END) AS CANCELLED_COUNT,
    ROUND(SUM(CASE WHEN STATE = 'SUCCEEDED' THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS SUCCESS_RATE,
    AVG(TIMESTAMPDIFF('second', SCHEDULED_TIME, COMPLETED_TIME)) AS AVG_DURATION_SEC,
    MAX(TIMESTAMPDIFF('second', SCHEDULED_TIME, COMPLETED_TIME)) AS MAX_DURATION_SEC
FROM SNOWFLAKE.ACCOUNT_USAGE.TASK_HISTORY
WHERE SCHEDULED_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
GROUP BY DATABASE_NAME, SCHEMA_NAME, NAME, STATE, DATE_TRUNC('day', SCHEDULED_TIME)
ORDER BY SCHEDULED_DATE DESC, FAILURE_COUNT DESC;

