-- 3.4 Storage Usage Trends
CREATE OR REPLACE VIEW V_STORAGE_USAGE_TRENDS AS
SELECT
    DATE_TRUNC('day', USAGE_DATE) AS REPORT_DATE,
    ROUND(AVG(STORAGE_BYTES)/1024/1024/1024/1024, 4) AS AVG_STORAGE_TB,
    ROUND(AVG(STAGE_BYTES)/1024/1024/1024/1024, 4) AS AVG_STAGE_TB,
    ROUND(AVG(FAILSAFE_BYTES)/1024/1024/1024/1024, 4) AS AVG_FAILSAFE_TB,
    ROUND((AVG(STORAGE_BYTES) + AVG(STAGE_BYTES) + AVG(FAILSAFE_BYTES))/1024/1024/1024/1024, 4) AS TOTAL_TB
FROM SNOWFLAKE.ACCOUNT_USAGE.STORAGE_USAGE
WHERE USAGE_DATE >= DATEADD('day', -90, CURRENT_TIMESTAMP())
GROUP BY DATE_TRUNC('day', USAGE_DATE)
ORDER BY REPORT_DATE DESC;

-- 3.5 Database Storage Breakdown
CREATE OR REPLACE VIEW V_DATABASE_STORAGE_BREAKDOWN AS
SELECT
    DATABASE_NAME,
    DATE_TRUNC('day', USAGE_DATE) AS REPORT_DATE,
    ROUND(AVG(AVERAGE_DATABASE_BYTES)/1024/1024/1024, 2) AS AVG_DB_SIZE_GB,
    ROUND(AVG(AVERAGE_FAILSAFE_BYTES)/1024/1024/1024, 2) AS AVG_FAILSAFE_GB,
    ROUND((AVG(AVERAGE_DATABASE_BYTES) + AVG(AVERAGE_FAILSAFE_BYTES))/1024/1024/1024, 2) AS TOTAL_GB
FROM SNOWFLAKE.ACCOUNT_USAGE.DATABASE_STORAGE_USAGE_HISTORY
WHERE USAGE_DATE >= DATEADD('day', -30, CURRENT_TIMESTAMP())
    AND DATABASE_NAME IS NOT NULL
GROUP BY DATABASE_NAME, DATE_TRUNC('day', USAGE_DATE)
ORDER BY REPORT_DATE DESC, TOTAL_GB DESC;

-- 3.6 Cost Per Query Analysis
CREATE OR REPLACE VIEW V_COST_PER_QUERY AS
SELECT
    QUERY_TYPE,
    USER_NAME,
    WAREHOUSE_NAME,
    DATE_TRUNC('day', START_TIME) AS QUERY_DATE,
    COUNT(*) AS QUERY_COUNT,
    AVG(TOTAL_ELAPSED_TIME)/1000 AS AVG_DURATION_SEC,
    SUM(BYTES_SCANNED)/1024/1024/1024 AS TOTAL_GB_SCANNED,
    ROUND(SUM(CREDITS_USED_CLOUD_SERVICES), 4) AS CLOUD_CREDITS,
    ROUND(SUM(CREDITS_USED_CLOUD_SERVICES)/NULLIF(COUNT(*), 0), 6) AS AVG_CREDIT_PER_QUERY
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
    AND QUERY_TYPE NOT IN ('UNKNOWN')
GROUP BY QUERY_TYPE, USER_NAME, WAREHOUSE_NAME, DATE_TRUNC('day', START_TIME)
ORDER BY CLOUD_CREDITS DESC;

-- 3.7 Monthly Cost Summary with Trending
CREATE OR REPLACE VIEW V_MONTHLY_COST_SUMMARY AS
SELECT
    DATE_TRUNC('month', START_TIME) AS MONTH,
    SUM(CREDITS_USED) AS TOTAL_CREDITS,
    SUM(CREDITS_USED_COMPUTE) AS COMPUTE_CREDITS,
    SUM(CREDITS_USED_CLOUD_SERVICES) AS CLOUD_SERVICES_CREDITS,
    LAG(SUM(CREDITS_USED)) OVER (ORDER BY DATE_TRUNC('month', START_TIME)) AS PREV_MONTH_CREDITS,
    ROUND((SUM(CREDITS_USED) - LAG(SUM(CREDITS_USED)) OVER (ORDER BY DATE_TRUNC('month', START_TIME))) 
        / NULLIF(LAG(SUM(CREDITS_USED)) OVER (ORDER BY DATE_TRUNC('month', START_TIME)), 0) * 100, 2) AS MOM_CHANGE_PCT
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD('month', -12, CURRENT_TIMESTAMP())
GROUP BY DATE_TRUNC('month', START_TIME)
ORDER BY MONTH DESC;

