-- 1.1 Warehouse Performance Summary (Last 7 Days)
CREATE OR REPLACE VIEW V_WAREHOUSE_PERFORMANCE_SUMMARY AS
SELECT
    WAREHOUSE_NAME,
    DATE_TRUNC('day', START_TIME) AS REPORT_DATE,
    COUNT(*) AS TOTAL_QUERIES,
    AVG(TOTAL_ELAPSED_TIME)/1000 AS AVG_ELAPSED_SEC,
    MEDIAN(TOTAL_ELAPSED_TIME)/1000 AS MEDIAN_ELAPSED_SEC,
    MAX(TOTAL_ELAPSED_TIME)/1000 AS MAX_ELAPSED_SEC,
    AVG(QUEUED_OVERLOAD_TIME)/1000 AS AVG_QUEUE_SEC,
    SUM(CASE WHEN QUEUED_OVERLOAD_TIME > 0 THEN 1 ELSE 0 END) AS QUEUED_QUERY_COUNT,
    ROUND(SUM(CASE WHEN QUEUED_OVERLOAD_TIME > 0 THEN 1 ELSE 0 END) * 100.0 / NULLIF(COUNT(*), 0), 2) AS QUEUE_PERCENTAGE,
    AVG(BYTES_SCANNED)/1024/1024/1024 AS AVG_GB_SCANNED,
    SUM(BYTES_SCANNED)/1024/1024/1024/1024 AS TOTAL_TB_SCANNED,
    AVG(ROWS_PRODUCED) AS AVG_ROWS_PRODUCED,
    COUNT(DISTINCT USER_NAME) AS UNIQUE_USERS,
    COUNT(DISTINCT QUERY_TAG) AS UNIQUE_QUERY_TAGS
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
    AND WAREHOUSE_NAME IS NOT NULL
GROUP BY WAREHOUSE_NAME, DATE_TRUNC('day', START_TIME)
ORDER BY REPORT_DATE DESC, TOTAL_QUERIES DESC;

-- 1.2 Query Performance Distribution
CREATE OR REPLACE VIEW V_QUERY_PERFORMANCE_DISTRIBUTION AS
SELECT
    WAREHOUSE_NAME,
    CASE
        WHEN TOTAL_ELAPSED_TIME < 1000 THEN '< 1 sec'
        WHEN TOTAL_ELAPSED_TIME < 5000 THEN '1-5 sec'
        WHEN TOTAL_ELAPSED_TIME < 30000 THEN '5-30 sec'
        WHEN TOTAL_ELAPSED_TIME < 60000 THEN '30-60 sec'
        WHEN TOTAL_ELAPSED_TIME < 300000 THEN '1-5 min'
        WHEN TOTAL_ELAPSED_TIME < 900000 THEN '5-15 min'
        ELSE '> 15 min'
    END AS DURATION_BUCKET,
    COUNT(*) AS QUERY_COUNT,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER (PARTITION BY WAREHOUSE_NAME), 2) AS PERCENTAGE
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
    AND WAREHOUSE_NAME IS NOT NULL
GROUP BY WAREHOUSE_NAME, DURATION_BUCKET
ORDER BY WAREHOUSE_NAME, 
    CASE DURATION_BUCKET
        WHEN '< 1 sec' THEN 1
        WHEN '1-5 sec' THEN 2
        WHEN '5-30 sec' THEN 3
        WHEN '30-60 sec' THEN 4
        WHEN '1-5 min' THEN 5
        WHEN '5-15 min' THEN 6
        ELSE 7
    END;

-- 1.3 Long Running Queries Monitor
CREATE OR REPLACE VIEW V_LONG_RUNNING_QUERIES AS
SELECT
