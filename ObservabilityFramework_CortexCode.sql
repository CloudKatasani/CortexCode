-- 9.1 Platform Health Score
CREATE OR REPLACE VIEW V_PLATFORM_HEALTH_SCORE AS
WITH metrics AS (
    SELECT
        -- Query Success Rate
        ROUND(SUM(CASE WHEN EXECUTION_STATUS = 'SUCCESS' THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 2) AS query_success_rate,
        -- Average Queue Time (target: < 5 seconds = 100 score)
        AVG(QUEUED_OVERLOAD_TIME)/1000 AS avg_queue_sec,
        -- Query count for volume
        COUNT(*) AS total_queries
    FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
    WHERE START_TIME >= DATEADD('hour', -24, CURRENT_TIMESTAMP())
),
task_metrics AS (
    SELECT
        ROUND(SUM(CASE WHEN STATE = 'SUCCEEDED' THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 2) AS task_success_rate
    FROM SNOWFLAKE.ACCOUNT_USAGE.TASK_HISTORY
    WHERE SCHEDULED_TIME >= DATEADD('hour', -24, CURRENT_TIMESTAMP())
),
login_metrics AS (
    SELECT
        ROUND(SUM(CASE WHEN IS_SUCCESS = 'YES' THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 2) AS login_success_rate
    FROM SNOWFLAKE.ACCOUNT_USAGE.LOGIN_HISTORY
    WHERE EVENT_TIMESTAMP >= DATEADD('hour', -24, CURRENT_TIMESTAMP())
)
SELECT
    CURRENT_TIMESTAMP() AS REPORT_TIME,
    '24 Hours' AS PERIOD,
    m.query_success_rate AS QUERY_SUCCESS_RATE,
    COALESCE(t.task_success_rate, 100) AS TASK_SUCCESS_RATE,
    l.login_success_rate AS LOGIN_SUCCESS_RATE,
    m.avg_queue_sec AS AVG_QUEUE_SECONDS,
    m.total_queries AS TOTAL_QUERIES,
    -- Overall Health Score (weighted average)
    ROUND(
        (m.query_success_rate * 0.4) +
        (COALESCE(t.task_success_rate, 100) * 0.3) +
        (l.login_success_rate * 0.2) +
        (GREATEST(0, 100 - (m.avg_queue_sec * 2)) * 0.1)
    , 2) AS OVERALL_HEALTH_SCORE
FROM metrics m
CROSS JOIN task_metrics t
CROSS JOIN login_metrics l;

