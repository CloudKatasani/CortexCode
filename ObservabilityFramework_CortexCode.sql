-- 1.5 Query Compilation vs Execution Time Analysis
CREATE OR REPLACE VIEW V_COMPILATION_EXECUTION_ANALYSIS AS
SELECT
    WAREHOUSE_NAME,
    DATE_TRUNC('hour', START_TIME) AS HOUR_BUCKET,
    COUNT(*) AS QUERY_COUNT,
    AVG(COMPILATION_TIME)/1000 AS AVG_COMPILE_SEC,
    AVG(EXECUTION_TIME)/1000 AS AVG_EXEC_SEC,
    AVG(QUEUED_PROVISIONING_TIME)/1000 AS AVG_QUEUE_PROVISION_SEC,
    AVG(QUEUED_REPAIR_TIME)/1000 AS AVG_QUEUE_REPAIR_SEC,
    AVG(QUEUED_OVERLOAD_TIME)/1000 AS AVG_QUEUE_OVERLOAD_SEC,
    ROUND(AVG(COMPILATION_TIME) * 100.0 / NULLIF(AVG(TOTAL_ELAPSED_TIME), 0), 2) AS COMPILE_PCT,
    ROUND(AVG(EXECUTION_TIME) * 100.0 / NULLIF(AVG(TOTAL_ELAPSED_TIME), 0), 2) AS EXEC_PCT
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE START_TIME >= DATEADD('day', -3, CURRENT_TIMESTAMP())
    AND WAREHOUSE_NAME IS NOT NULL
GROUP BY WAREHOUSE_NAME, DATE_TRUNC('hour', START_TIME)
ORDER BY HOUR_BUCKET DESC;

