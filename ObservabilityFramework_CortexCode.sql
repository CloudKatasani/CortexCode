-- 10.4 Naming Convention Compliance
CREATE OR REPLACE VIEW V_NAMING_CONVENTION_COMPLIANCE AS
SELECT
    'DATABASE' AS OBJECT_TYPE,
    DATABASE_NAME AS OBJECT_NAME,
    CASE 
        WHEN DATABASE_NAME RLIKE '^[A-Z][A-Z0-9_]*$' THEN 'COMPLIANT'
        ELSE 'NON_COMPLIANT'
    END AS NAMING_STATUS,
    'Must be uppercase, start with letter, contain only letters, numbers, underscores' AS CONVENTION
FROM SNOWFLAKE.ACCOUNT_USAGE.DATABASES
WHERE DELETED IS NULL

UNION ALL

SELECT
    'TABLE' AS OBJECT_TYPE,
    TABLE_CATALOG || '.' || TABLE_SCHEMA || '.' || TABLE_NAME AS OBJECT_NAME,
    CASE 
        WHEN TABLE_NAME RLIKE '^[A-Z][A-Z0-9_]*$' THEN 'COMPLIANT'
        ELSE 'NON_COMPLIANT'
    END AS NAMING_STATUS,
    'Must be uppercase, start with letter, contain only letters, numbers, underscores' AS CONVENTION
FROM SNOWFLAKE.ACCOUNT_USAGE.TABLES
WHERE DELETED IS NULL
    AND TABLE_SCHEMA NOT IN ('INFORMATION_SCHEMA');

-- 10.5 Resource Usage Governance
CREATE OR REPLACE VIEW V_RESOURCE_GOVERNANCE AS
SELECT
    WAREHOUSE_NAME,
    MAX(WAREHOUSE_SIZE) AS MAX_SIZE_USED,
    SUM(CREDITS_USED) AS TOTAL_CREDITS_7D,
    COUNT(DISTINCT DATE_TRUNC('day', START_TIME)) AS ACTIVE_DAYS,
    ROUND(AVG(CREDITS_USED), 4) AS AVG_HOURLY_CREDITS,
    CASE
        WHEN SUM(CREDITS_USED) > 100 THEN 'HIGH_CONSUMPTION'
        WHEN SUM(CREDITS_USED) > 50 THEN 'MODERATE_CONSUMPTION'
        ELSE 'LOW_CONSUMPTION'
    END AS CONSUMPTION_TIER
FROM SNOWFLAKE.ACCOUNT_USAGE.WAREHOUSE_METERING_HISTORY
WHERE START_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
GROUP BY WAREHOUSE_NAME
ORDER BY TOTAL_CREDITS_7D DESC;

-- 10.6 Automation: Cleanup Procedure for Old Data
CREATE OR REPLACE PROCEDURE SP_CLEANUP_OBSERVABILITY_DATA(P_RETENTION_DAYS NUMBER)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
DECLARE
    V_DELETED_ALERTS NUMBER;
    V_DELETED_QUALITY NUMBER;
    V_DELETED_AUDIT NUMBER;
BEGIN
    -- Cleanup old alert history
    DELETE FROM T_ALERT_HISTORY 
    WHERE TRIGGERED_AT < DATEADD('day', -P_RETENTION_DAYS, CURRENT_TIMESTAMP());
    V_DELETED_ALERTS := SQLROWCOUNT;
    
    -- Cleanup old quality results
    DELETE FROM T_DATA_QUALITY_RESULTS 
    WHERE EXECUTION_TIME < DATEADD('day', -P_RETENTION_DAYS, CURRENT_TIMESTAMP());
    V_DELETED_QUALITY := SQLROWCOUNT;
    
    -- Cleanup old audit logs
    DELETE FROM T_GOVERNANCE_AUDIT_LOG 
    WHERE CHECK_TIME < DATEADD('day', -P_RETENTION_DAYS, CURRENT_TIMESTAMP());
    V_DELETED_AUDIT := SQLROWCOUNT;
    
    RETURN 'Cleanup completed. Deleted: ' || V_DELETED_ALERTS || ' alerts, ' || 
           V_DELETED_QUALITY || ' quality results, ' || V_DELETED_AUDIT || ' audit logs';
END;
$$;

