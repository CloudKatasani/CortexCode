-- 9.5 SLA Compliance Dashboard
CREATE OR REPLACE VIEW V_SLA_COMPLIANCE AS
WITH sla_metrics AS (
    SELECT
        DATE_TRUNC('day', START_TIME) AS REPORT_DATE,
        -- Query SLA: 95% of queries complete in < 60 seconds
        ROUND(SUM(CASE WHEN TOTAL_ELAPSED_TIME < 60000 THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 2) AS QUERIES_UNDER_60S_PCT,
        -- Availability SLA: 99.9% query success
        ROUND(SUM(CASE WHEN EXECUTION_STATUS = 'SUCCESS' THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 4) AS QUERY_SUCCESS_PCT,
        -- Queue SLA: 90% of queries with < 5 second queue time
        ROUND(SUM(CASE WHEN QUEUED_OVERLOAD_TIME < 5000 THEN 1 ELSE 0 END) * 100.0 / 
              NULLIF(COUNT(*), 0), 2) AS QUERIES_LOW_QUEUE_PCT
    FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
    WHERE START_TIME >= DATEADD('day', -30, CURRENT_TIMESTAMP())
    GROUP BY DATE_TRUNC('day', START_TIME)
)
SELECT
    REPORT_DATE,
    QUERIES_UNDER_60S_PCT,
    CASE WHEN QUERIES_UNDER_60S_PCT >= 95 THEN 'MET' ELSE 'MISSED' END AS PERFORMANCE_SLA,
    QUERY_SUCCESS_PCT,
    CASE WHEN QUERY_SUCCESS_PCT >= 99.9 THEN 'MET' ELSE 'MISSED' END AS AVAILABILITY_SLA,
    QUERIES_LOW_QUEUE_PCT,
    CASE WHEN QUERIES_LOW_QUEUE_PCT >= 90 THEN 'MET' ELSE 'MISSED' END AS QUEUE_SLA
FROM sla_metrics
ORDER BY REPORT_DATE DESC;

