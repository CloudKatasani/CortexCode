-- 4.3 Procedure: Execute Data Quality Check
CREATE OR REPLACE PROCEDURE SP_RUN_DATA_QUALITY_CHECK(
    P_DATABASE VARCHAR,
    P_SCHEMA VARCHAR,
    P_TABLE VARCHAR
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
DECLARE
    V_RESULT VARCHAR DEFAULT 'Data quality check completed';
    V_TOTAL_ROWS NUMBER;
    V_NULL_COUNT NUMBER;
BEGIN
    -- Get total row count
    EXECUTE IMMEDIATE 'SELECT COUNT(*) FROM ' || P_DATABASE || '.' || P_SCHEMA || '.' || P_TABLE 
        INTO :V_TOTAL_ROWS;
    
    RETURN V_RESULT || ' for ' || P_DATABASE || '.' || P_SCHEMA || '.' || P_TABLE || 
           ' (Total Rows: ' || V_TOTAL_ROWS || ')';
END;
$$;

-- 4.4 Schema Change Detection View
CREATE OR REPLACE VIEW V_SCHEMA_CHANGES AS
SELECT
    EVENT_TIMESTAMP,
    USER_NAME,
    QUERY_TYPE,
    DATABASE_NAME,
    SCHEMA_NAME,
    QUERY_TEXT,
    CASE
        WHEN QUERY_TYPE IN ('CREATE_TABLE', 'CREATE_VIEW', 'CREATE_SCHEMA') THEN 'OBJECT_CREATED'
        WHEN QUERY_TYPE IN ('DROP_TABLE', 'DROP_VIEW', 'DROP_SCHEMA') THEN 'OBJECT_DROPPED'
        WHEN QUERY_TYPE IN ('ALTER_TABLE', 'RENAME_TABLE') THEN 'OBJECT_MODIFIED'
        ELSE 'OTHER'
    END AS CHANGE_TYPE
FROM SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE QUERY_TYPE IN (
    'CREATE_TABLE', 'CREATE_VIEW', 'CREATE_SCHEMA', 'CREATE_DATABASE',
    'DROP_TABLE', 'DROP_VIEW', 'DROP_SCHEMA', 'DROP_DATABASE',
    'ALTER_TABLE', 'RENAME_TABLE', 'ALTER_VIEW'
)
AND START_TIME >= DATEADD('day', -30, CURRENT_TIMESTAMP())
ORDER BY EVENT_TIMESTAMP DESC;

