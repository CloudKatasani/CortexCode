-- 2.3 Table Freshness Monitor (Custom Implementation)
CREATE OR REPLACE VIEW V_TABLE_FRESHNESS AS
SELECT
    t.TABLE_CATALOG AS DATABASE_NAME,
    t.TABLE_SCHEMA AS SCHEMA_NAME,
    t.TABLE_NAME,
    t.TABLE_TYPE,
    t.ROW_COUNT,
    t.BYTES / 1024 / 1024 AS SIZE_MB,
    t.LAST_ALTERED AS LAST_DDL_CHANGE,
    TIMESTAMPDIFF('hour', t.LAST_ALTERED, CURRENT_TIMESTAMP()) AS HOURS_SINCE_LAST_CHANGE,
    CASE
        WHEN TIMESTAMPDIFF('hour', t.LAST_ALTERED, CURRENT_TIMESTAMP()) <= 1 THEN 'FRESH'
        WHEN TIMESTAMPDIFF('hour', t.LAST_ALTERED, CURRENT_TIMESTAMP()) <= 24 THEN 'RECENT'
        WHEN TIMESTAMPDIFF('hour', t.LAST_ALTERED, CURRENT_TIMESTAMP()) <= 168 THEN 'STALE'
        ELSE 'VERY_STALE'
    END AS FRESHNESS_STATUS
FROM SNOWFLAKE_LEARNING_DB.INFORMATION_SCHEMA.TABLES t
WHERE t.TABLE_SCHEMA NOT IN ('INFORMATION_SCHEMA')
ORDER BY HOURS_SINCE_LAST_CHANGE DESC;

-- 2.4 Copy History Summary (Data Ingestion)
CREATE OR REPLACE VIEW V_COPY_HISTORY_SUMMARY AS
SELECT
    TABLE_CATALOG_NAME AS DATABASE_NAME,
    TABLE_SCHEMA_NAME AS SCHEMA_NAME,
    TABLE_NAME,
    DATE_TRUNC('hour', LAST_LOAD_TIME) AS LOAD_HOUR,
    COUNT(*) AS LOAD_COUNT,
    SUM(ROW_COUNT) AS TOTAL_ROWS_LOADED,
    SUM(FILE_SIZE)/1024/1024 AS TOTAL_MB_LOADED,
    SUM(CASE WHEN STATUS = 'LOADED' THEN 1 ELSE 0 END) AS SUCCESS_COUNT,
    SUM(CASE WHEN STATUS != 'LOADED' THEN 1 ELSE 0 END) AS ERROR_COUNT,
    COUNT(DISTINCT FILE_NAME) AS FILES_PROCESSED
FROM SNOWFLAKE.ACCOUNT_USAGE.COPY_HISTORY
WHERE LAST_LOAD_TIME >= DATEADD('day', -7, CURRENT_TIMESTAMP())
GROUP BY TABLE_CATALOG_NAME, TABLE_SCHEMA_NAME, TABLE_NAME, DATE_TRUNC('hour', LAST_LOAD_TIME)
ORDER BY LOAD_HOUR DESC;

